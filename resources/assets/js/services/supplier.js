const ENDPOINT_GET = '/admin/suppliers',
      ENDPOINT_GET_ID = '/admin/suppliers/byId',
      ENDPOINT_FETCH = '/admin/suppliers/{id}',
      ENDPOINT_CREATE = '/admin/suppliers/create',
      ENDPOINT_SEARCH = '/admin/suppliers/search'

export default class {

  constructor (app) {
    this.$http = app.$http
  }

  get (params) {
    return this.$http.get(ENDPOINT_GET, { params })
  }

  getById (id, params = {}) {
    params.id = id

    return this.$http.get(ENDPOINT_GET_ID, { params })
  }

  fetch (id, params) {
    let url = ENDPOINT_FETCH.formatUnicorn({ id })

    return this.$http.get(url, { params })
  }

  search (params) {
    return this.$http.get(ENDPOINT_SEARCH, { params })
  }

  create (data) {
    return this.$http.post(ENDPOINT_CREATE, data)
  }

  update (data) {
    let id = data.id, url = ENDPOINT_FETCH.formatUnicorn({ id })

    return this.$http.patch(url, data)
  }

  delete (id) {
    let url = ENDPOINT_FETCH.formatUnicorn({ id })
    
    return this.$http.delete(url)
  }

}